let x = Math.floor(Math.random() * 100);

let y = Math.floor(Math.random() * 50);

let sum = x + y; 

let difference = x - y;

let product = x * y;

let quotient = x/y; 

let remainder = x % y;


console.log(`${x} + ${y} = ${sum}`);
console.log(`${x} - ${y} = ${difference}`);
console.log(`${x} * ${y} = ${product}`);
console.log(`${x} / ${y} = ${quotient}`);
console.log(`${x} % ${y} = ${remainder}`);